

public class CCA 
{
	public String title;
	public String description;
	public int classSize;
	public String day;
	public String time;
	public String venue;
	public String nameOfInstructor;
	public String idOfCCA;
	
	
	public CCA(String title, String descp, int classSize, String Time,String day, String venue, 
			String NameOfInstructor,String idOfCCA) 
	
		{
		    this.title = title;
		    this.description = descp;
		    this.classSize = classSize;
		    this.time = Time;
		    this.venue = venue;
		    this.day = day;
		    this.nameOfInstructor = NameOfInstructor;
		    this.idOfCCA = idOfCCA;
		}
}
