

public class Student
{
	
		public int age;
		public String studentId;
		public String userName;
		public String passWord;
		public String name;
		public String grade;
		public String teacher;
		public String parentName;
		public String parentEmail;
		public int number;
		public String ccaCat;
		public String ccaId;
		
		public Student(int age, String studentId, String userName, String passWord, String name, 
				String grade, String teacher, String parentName,
				String parentEmail, int number) 
		{
		
			this.age = age;
			this.studentId = studentId;
			this.userName = userName;
			this.passWord = passWord;
			this.name = name;
			this.grade = grade;
			this.teacher = teacher;
			this.parentName = parentName;
			this.parentEmail = parentEmail;
			this.number = number;
			this.ccaCat="";
			this.ccaId = "";
		}



}
